    public function categoryPost($slug){
       $category =  Category::whereSlug($slug)->first();
       $posts = Post::whereCategoryId($category->id)->with('user')->get();
       foreach($posts as $post){
        $post->setAttribute('comments_count',$post->comments->count());
       }

